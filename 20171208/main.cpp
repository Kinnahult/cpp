#include <iostream>
using namespace std;
int main()
{
  // iteration (loop
  // med for-sats
  // 8.1, sidan 105 
int n=0; 
  while (n<100)
  {
    cout << "(for) n=" << n << endl;
    n=n+1;
  }
  
  
  
  
  //   (startvärde; villkor; förändring
  for (int n=0; n<100; n++)
  {
    cout << "(for) n=" << n << endl;
  }
  
  cout << "Med while-sats" << endl;
  int o=0;
  while (o<100)
  {
    cout << "(while) o=" << o << endl;
    o++; // o=o+1;
  }
  
 
  return 0;
  }
    
    

